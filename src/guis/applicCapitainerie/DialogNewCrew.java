    private void jButtonCommitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCommitActionPerformed
        if(listMarin.getSize() < 2) {
            javax.swing.JOptionPane.showMessageDialog(null, "Il faut au minimum un capitaine et un second");
        }
        else {
            team = new Equipage((Marin)listMarin.getElementAt(0), (Marin)listMarin.getElementAt(1));
            
            for (int i = 2; i < listMarin.getSize(); i++) {
                team.ajouter((Marin)listMarin.getElementAt(i));
            }
            
            ok = true;
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButtonCommitActionPerformed

    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        if(jTextFieldNom.getText().equals("") && jTextFieldPrenom.getText().equals(""))
            javax.swing.JOptionPane.showMessageDialog(null, "Veuillez completer tous les champs");
        else {
            try {
                Marin m = new Marin(jTextFieldNom.getText(), jTextFieldPrenom.getText(),
                        jXDatePicker.getDate().toString(),
                        (Marin.typeFonction)jComboBox1.getSelectedItem());
                
                listMarin.addElement(m);
            } 
            catch (SailorWithoutIdentificationException ex) {
                Logger.getLogger(DialogNewCrew.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            jTextFieldNom.setText("");
            jTextFieldPrenom.setText("");
            jXDatePicker.setDate(null);
            
            if(listMarin.getSize() == 1) {
                comboList.removeAllElements();
                comboList.addElement(Marin.typeFonction.second);
            }
            else if(listMarin.getSize() == 2) {
                comboList.removeAllElements();
                comboList.addElement(Marin.typeFonction.bosco);
                comboList.addElement(Marin.typeFonction.matelot);
                comboList.addElement(Marin.typeFonction.passager);
            }
        }
    }//GEN-LAST:event_jButtonOkActionPerformed

