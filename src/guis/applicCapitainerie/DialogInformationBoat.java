    private void jButtonCrewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrewActionPerformed
        DialogNewCrew dia = new DialogNewCrew((java.awt.Frame)this.getParent(), true, jLabelBoat.getText());
        
        dia.setVisible(true);
        if(dia.ok){
            crew = dia.team;
            
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            model.addElement(crew.getCapitaine());
            model.addElement(crew.getSecond());
            
            for (int i = 0; i < crew.getNombreMarin(); i++) {
                model.addElement(crew.getMarin(i));
            }
            
            jComboBoxCrew.setModel(model);
        }
    }//GEN-LAST:event_jButtonCrewActionPerformed

