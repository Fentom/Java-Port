    public DialogInformationBoat(java.awt.Frame parent, boolean modal, Arrivee a) {
        this(parent, modal);
        
        ArrayList<Integer> emp = new ArrayList<>();
            
        for (String ind : a.getEmplacement().split("-")) {
            emp.add(Integer.parseInt(ind));
        }
        
        if(a.getBoat() instanceof BateauPeche) {
            jLabelEmplacement.setText("Q"+(emp.get(0)+1)+"-"+(emp.get(1)+1));
        }
        else if(a.getBoat() instanceof BateauPlaisance) {
            jLabelEmplacement.setText("P"+(emp.get(0)+1)+"-"+(emp.get(1)+1));
        }
        
        jLabelBoat.setText(a.getBoat().getNom());
        jLabelPavillon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagesFlag/"+a.getBoat().getPavillon().toString()+".jpg")));
    }

