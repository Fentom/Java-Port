    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        if(crew == null)
            javax.swing.JOptionPane.showMessageDialog(null, "Veuillez preciser l'equipage");
        else if(jTextFieldPortAttache.getText().equals("") && jTextFieldTonnage.getText().equals("")) {
            javax.swing.JOptionPane.showMessageDialog(null, "Veuillez completer tous les champs");
        }
        else {
            tonnage = Float.parseFloat(jTextFieldTonnage.getText());
            portAttache = jTextFieldPortAttache.getText();
            
            ok = true;
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButtonOkActionPerformed

